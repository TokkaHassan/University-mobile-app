package com.example.graduationapplication.studentStuff.Recycler_classes;

public class Contacts_form {
    String name;
    String mobile;

    public Contacts_form(String name, String mobile) {
        this.name = name;
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }


}
